declare(strict_types=1);

namespace PersiLiao\GitWebhooks\Event;

class PingEvent extends AbstractEvent
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $title;

    /**
     * @var string
     */
    private $description;

    /**
     * @var string
     */
    private $action;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return PingEvent
     */
    public function setId(int $id): PingEvent
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @param string $title
     * @return PingEvent
     */
    public function setTitle(string $title): PingEvent
    {
        $this->title = $title;
        return $this;
    }

    /**
     * @return string
     */
    public function getDescription(): string
    {
        return $this->description;
    }

    /**
     * @param string $description
     * @return PingEvent
     */
    public function setDescription(string $description): PingEvent
    {
        $this->description = $description;
        return $this;
    }

    /**
     * @return string
     */
    public function getAction(): string
    {
        return $this->action;
    }

    /**
     * @param string $action
     * @return PingEvent
     */
    public function setAction(string $action): PingEvent
    {
        $this->action = $action;
        return $this;
    }
}